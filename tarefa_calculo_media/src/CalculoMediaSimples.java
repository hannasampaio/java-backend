public class CalculoMediaSimples {
    public static void main(String[] args) {
        int nota1 = 7;
        int nota2 = 8;
        int nota3 = 9;
        int nota4 = 6;

        int media = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.println("Notas: " + nota1 + ", " + nota2 + ", " + nota3 + ", " + nota4);
        System.out.println("A média é " + media);
    }
}
